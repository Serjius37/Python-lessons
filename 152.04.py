#!/usr/bin/python3
'''
Тимур загадал число от 11 до nn. За какое наименьшее количество вопросов 
(на которые Тимур отвечает "больше" или "меньше") Руслан может гарантированно угадать число Тимура?
Формат входных данных
На вход программе подается натуральное число nn.
Формат выходных данных
Программа должна вывести наименьшее количество вопросов, которых гарантированно хватит Руслану, чтобы угадать число Тимура.
'''

a = int(input())
b = 1
while a > 2:
    a /= 2
    b += 1
print(b)